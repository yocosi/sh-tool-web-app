    private final UserService userService;
    private final EmailValidator emailValidator;
    private final ConfirmationTokenService confirmationTokenService;
    private final EmailSender emailSender;

    public String register(RegistrationRequest request){
        LocalDate lt = LocalDate.now();
        String token = userService.signUpUser(new User(request.getUsername(), request.getEmail(), request.getPassword(), lt, UserRole.USER));
        String link = "http://localhost:8080/api/v1/registration/confirm?token=" + token;
        boolean isValidEmail = emailValidator.test(request.getEmail());
        if (!isValidEmail){
            throw new IllegalStateException("error: the email is not valid.");
        }
        emailSender.send(request.getEmail(), buildEmail(request.getUsername(), link));
        return token;
    }

    @Transactional
    public String confirmToken(String token) {
        ConfirmationToken confirmationToken = confirmationTokenService
                .getToken(token)
                .orElseThrow(() ->
                        new IllegalStateException("error: token not found"));

        if (confirmationToken.getConfirmedAt() != null) {
            throw new IllegalStateException("error: email already confirmed");
        }

        LocalDateTime expiredAt = confirmationToken.getExpiresAt();

        if (expiredAt.isBefore(LocalDateTime.now())) {
            throw new IllegalStateException("error: token expired");
        }

        confirmationTokenService.setConfirmedAt(token);
        userService.enableUser(
                confirmationToken.getUser().getEmail());
        return "confirmed";
    }

